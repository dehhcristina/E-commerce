package web;

import beans.ListarProdutosBeanRemote;
import com.fasterxml.jackson.databind.ObjectMapper;
import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.PrintWriter;
import java.io.StringReader;
import java.util.stream.Collectors;
import javax.ejb.EJB;
import javax.json.Json;
import javax.json.JsonObject;
import javax.json.JsonReader;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author Loren
 */
public class PesquisarProdutosServlet extends HttpServlet {

    @EJB
    ListarProdutosBeanRemote bean;

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("application/json");
        PrintWriter out = response.getWriter();
        String textJSON;
        ObjectMapper mapper = new ObjectMapper();
        BufferedReader leitor = new BufferedReader(new InputStreamReader(request.getInputStream(), "UTF-8"));
        textJSON = leitor.lines().collect(Collectors.joining());
        JsonReader reader = Json.createReader(new StringReader(textJSON));
        JsonObject dataJSON = reader.readObject();
        String PESQUISA;
        JsonObject retorno;
        PESQUISA = dataJSON.getJsonString("PESQUISA").getString();

        try {
            retorno = Json.createObjectBuilder()
                    .add("produtos", mapper.writeValueAsString(bean.pesquisarProdutos(PESQUISA)))
                    .build();

        } catch (Exception ex) {
            retorno = Json.createObjectBuilder()
                    .add("message", ex.getMessage())
                    .build();
            response.setStatus(500);
        }

        out.println(retorno.toString());
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
